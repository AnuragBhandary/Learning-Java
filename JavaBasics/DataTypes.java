package JavaBasics;

public class DataTypes {

	public static void main(String[] args) {
		// integer 
		int i = 1;
		
		// String
		String a = "Hello";
		
		// boolean
		boolean b = true;
		
		// character
		char c = 'r';
		
		// double
		double d = 12.33;
		
		System.out.println(i);
		System.out.println(a);
		System.out.println(b);
		System.out.println(c);
		System.out.println(d);

	}

}
